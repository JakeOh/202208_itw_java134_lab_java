package edu.java.loop06;

public class LoopMain06 {

    public static void main(String[] args) {
        // for 반복문
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        
        System.out.println("-----");
        // while 반복문
        int n = 1;
        while (n <= 5) {
            System.out.println(n);
            n++;
        }
        
    }

}
